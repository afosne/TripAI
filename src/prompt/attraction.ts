import type { ChatMessage } from '../services/openai'

export function buildAttractionDetailPrompt(name: string, city?: string): ChatMessage {
  return {
    role: 'user',
    content: `请为景点「${name}」${city ? `（位于${city}）` : ''}生成一份详细的游玩攻略，返回 JSON 格式：

- how_to_play: 详细游玩攻略（4-6句话）。包括推荐游览路线（如从哪个门进、先看什么后看什么）、核心游览区域和体验项目的介绍，让用户对怎么逛有一个清晰的路线规划
- best_time: 最佳游览时间建议（2-3句话）。包括一天中什么时段去最好、哪个季节最合适、是否需要避开节假日高峰等
- recommended_duration: 建议游览时长（如"建议预留3-4小时"、"建议安排一整天"）
- how_to_get: 交通指引（3-4句话）。包括最近的地铁/公交站点及具体线路、打车定位建议、是否有专用停车场等实用信息
- ticket_info: 门票信息（2-3句话）。包括门票价格（成人/学生/老人）、是否需要提前预约、开放时间、免费政策等
- tips: 实用贴士（4-6句话）。包括游览注意事项（穿着、防晒、带水等）、内部是否需要接驳车/索道等额外费用、餐饮建议（景区内是否有餐厅/是否可以自带食物）、拍照最佳位置、容易踩的坑（如某些区域会关门、排队技巧等）
- highlights: 必看亮点列表（4-6个，数组）。列出该景点最值得看的、不可错过的核心体验或景观
- nearby: 附近推荐（4-6个，数组）。列出周边值得顺便去的景点、商圈或美食街，附大致距离（如"步行10分钟"）`,
  }
}
